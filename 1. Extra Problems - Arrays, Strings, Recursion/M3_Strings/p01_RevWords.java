package ProblemSolving.M3_Strings;

import java.util.*;

public class p01_RevWords {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        
        String str = sc.next();

        String[] arrOfStr = str.split(".");
  
        for (String a : arrOfStr)
            System.out.println(a);
        


        sc.close();
    }
}


































